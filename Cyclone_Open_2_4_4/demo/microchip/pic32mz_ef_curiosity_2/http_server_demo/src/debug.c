//Dependencies
#include <p32xxxx.h>
#include "debug.h"


/**
 * @brief Debug UART initialization
 * @param[in] baudrate UART baudrate
 **/

void debugInit(uint32_t baudrate)
{
   //Disable analog pad on U6RX (AN30/RF13)
   ANSELFCLR = _ANSELF_ANSF13_MASK;

   //Remap U6TX to RPF2
   RPF2R = 4;
   //Remap U6RX to RPF13
   U6RXR = 9;

   //Configure UART2 (8 bits, no parity, 1 stop bit)
   U6STA = 0;
   //Enable high-speed mode
   U6MODE = _U6MODE_BRGH_MASK;
   //Set baudrate
   U6BRG = ((39600000 / 4) / baudrate) - 1;

   //Enable UART2
   U6MODESET = _U6MODE_ON_MASK;
   //Enable transmitter and receiver
   U6STASET = _U6STA_UTXEN_MASK | _U6STA_URXEN_MASK;
}


/**
 * @brief Display the contents of an array
 * @param[in] stream Pointer to a FILE object that identifies an output stream
 * @param[in] prepend String to prepend to the left of each line
 * @param[in] data Pointer to the data array
 * @param[in] length Number of bytes to display
 **/

void debugDisplayArray(FILE *stream,
   const char_t *prepend, const void *data, size_t length)
{
   uint_t i;

   for(i = 0; i < length; i++)
   {
      //Beginning of a new line?
      if((i % 16) == 0)
         fprintf(stream, "%s", prepend);
      //Display current data byte
      fprintf(stream, "%02" PRIX8 " ", *((uint8_t *) data + i));
      //End of current line?
      if((i % 16) == 15 || i == (length - 1))
         fprintf(stream, "\r\n");
   }
}


/**
 * @brief Write character to stream
 * @param[in] c The character to be written
 * @param[in] stream Pointer to a FILE object that identifies an output stream
 * @return On success, the character written is returned. If a writing
 *   error occurs, EOF is returned
 **/

int_t fputc(int_t c, FILE *stream)
{
   //Standard output or error output?
   if(stream == stdout || stream == stderr)
   {
      //Wait for the transmitter to be ready
      while(U6STA & _U6STA_UTXBF_MASK);
      //Send character
      U6TXREG = c;
      //Wait for the transfer to complete
      while(!(U6STA & _U6STA_TRMT_MASK));

      //On success, the character written is returned
      return c;
   }
   //Unknown output?
   else
   {
      //If a writing error occurs, EOF is returned
      return EOF;
   }
}
